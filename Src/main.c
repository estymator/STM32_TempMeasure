/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{

	if((receive[counter])=='\n')
	{
		if(overflow)
		{
			HAL_UART_Transmit( &huart2, (uint8_t*)"Przekroczono bufor wejsciowy\n",29, 10 );
			overflow=0;
			counter=0;
			HAL_UART_Receive_IT(&huart2, receive, 1);
		}else{
			HAL_UART_Receive_IT(&huart2, receive, 1);
			Parser(receive, counter);
			counter=0;
		}
	}else if(counter>=BUF_SIZE) //zmien flage przepe³nienia i wczytaj tekst do konca
	{
		overflow=1;
		counter=0;
		HAL_UART_Receive_IT(&huart2, receive, 1);
	}else  //wczytaj nastepny znak wejscia
	{
		counter++;
		HAL_UART_Receive_IT(&huart2, receive + counter, 1);
	}

}



void Parser(uint8_t* mess, uint8_t counter)
{
	const char t_cmd[]="temp";
	int freq, amount;
	if(memcmp(mess, t_cmd, strlen(t_cmd))==0 && counter==4) //odczyt co sekunde w nieskonczonosc
	{
		HAL_GPIO_WritePin(GPIOA, DS18B20_Pin|LD2_Pin, GPIO_PIN_RESET);
		inf=1;
		freq=1000;
		measure_number=1;
		htim3.Init.Period=freq;
		HAL_TIM_Base_Init(&htim3);
		HAL_TIM_Base_Start_IT(&htim3);
	}
	else if(memcmp(mess, t_cmd, strlen(t_cmd))==0)
	{
		inf=0;
		HAL_GPIO_WritePin(GPIOA, DS18B20_Pin|LD2_Pin, GPIO_PIN_SET);
		sscanf((char*)(mess + strlen(t_cmd))," %d %d", &freq, &amount);

		measure_number=amount;
		if (freq < 2)		freq = 1;
		else		freq = freq - 1;

		htim3.Init.Period=freq;
		HAL_TIM_Base_Init(&htim3);
		HAL_TIM_Base_Start_IT(&htim3);



	}else
	{
		inf=0;

	}
}

void getTemp()
 {
	 DS18B20_ReadAll(); //wykrywanie wszystkich czujników

	 DS18B20_StartAll(); //active
	 uint8_t ROM_tmp[8];
	 uint8_t i;
	 for(i = 0; i < DS18B20_Quantity(); i++)  //pêtla po wszystkich czujnikach
	 {
	 	 if(DS18B20_GetTemperature(i, &temperature))
	 	 {
	 		 DS18B20_GetROM(i, ROM_tmp);
	 		 //HAL_GPIO_TogglePin(GPIOA, DS18B20_Pin|LD2_Pin);
	 		 int a = sprintf(message, "Temp %f\n\r", temperature);

	 		 HAL_UART_Transmit(&huart2, (uint8_t*)message, a, 100);



	 	 }
	 }
	 HAL_UART_Transmit(&huart2, (uint8_t*)"\n\r", sizeof("\n\r")-1, 100);

 }

